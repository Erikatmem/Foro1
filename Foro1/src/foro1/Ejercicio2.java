package foro1;
//Un bucle for (inicialización; condición; actualización)es como un camino circular que recorres una y otra vez, siempre que se cumpla una condición
//Inicialización: Es el primer paso y se ejecuta solo una vez. Aquí se declara y se inicializa la variable de control.
//Condición: Es una expresión booleana que se evalúa antes de cada iteración. Si la condición es verdadera, se ejecuta el bloque de código. Si es falsa, se termina el bucle.
//Actualización: Aquí se actualiza la variable de control después de cada iteración.

public class Ejercicio2 {
	
	    public static void main(String[] args) {
	        for (int vuelta = 0; vuelta < 15; vuelta++) {
	            System.out.println("Estoy en la vuelta número: " + vuelta);
	        }
	    }
	}

